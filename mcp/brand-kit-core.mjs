// brand-kit-core.mjs — the PURE, transport-agnostic MCP surface for an Ultimate Tokens brand kit. NO I/O.
//
// Given a resolved kit object, `buildSurface(kit)` returns the tools/resources/prompts (the EXACT surface
// the downloadable stdio server and the hosted Cloudflare Worker both serve), and `handle(msg, surface)` is
// the pure JSON-RPC 2.0 dispatch — it RETURNS a response object (or null when there's nothing to send: a
// notification / notification-shaped ping), leaving the transport (stdout, HTTP) to the caller.
//
// brand-kit-server.mjs (stdio) imports this; the hosted MCP Worker imports the SAME module — so the surface
// can't drift between the downloaded and hosted servers. Parity by construction (see test/mcp/core.mjs).

export const PROTOCOL_VERSION = "2025-06-18";
export const SERVER = { name: "ultimate-tokens-brand-kit", version: "0.1.0" };

const slugOf = (s) => String(s || "").toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/(^-|-$)/g, "");
const hexToRgb = (h) => { const m = String(h).replace("#", ""); return [0, 2, 4].map((i) => parseInt(m.slice(i, i + 2) || "0", 16)); };

// buildSurface(kit) → { kit, palettes, hasColor, TOOLS, RESOURCES, PROMPTS, SERVER, PROTOCOL_VERSION }.
// Each token SYSTEM is opt-in at export time, so the surface reflects what's PRESENT: the colour
// tools/resources appear only when the kit has palettes/roles; type/geometry only when those were included.
export function buildSurface(kit) {
  kit = kit && typeof kit === "object" ? kit : {};
  const palettes = Array.isArray(kit.palettes) ? kit.palettes : [];
  const roles = kit.roles || {};
  const hasColor = palettes.length > 0 || Object.keys(roles).length > 0;

  const findPalette = (name) => {
    const q = slugOf(name);
    return palettes.find((p) => slugOf(p.name) === q || slugOf(p.slug) === q) || null;
  };
  function nearestToken(hex) {
    const [r, g, b] = hexToRgb(hex);
    let best = null;
    for (const p of palettes) for (const s of (p.ramp || [])) {
      const [r2, g2, b2] = hexToRgb(s.hex);
      const d = (r - r2) ** 2 + (g - g2) ** 2 + (b - b2) ** 2;
      if (!best || d < best.d) best = { d, palette: p.name, stop: s.stop, hex: s.hex };
    }
    return best && { palette: best.palette, stop: best.stop, hex: best.hex, distance: Math.round(Math.sqrt(best.d)) };
  }
  const semanticFor = (scheme) => {
    const out = {};
    for (const [slug, rs] of Object.entries(roles)) for (const [key, v] of Object.entries(rs)) out[`${slug}/${key}`] = v[scheme] ?? v.light;
    return out;
  };
  const usageGuide = () => {
    const hasColorG = palettes.length > 0 || Object.keys(roles).length > 0;
    let g = `# ${kit.name || "Brand Kit"} — usage\n\n` +
      `Generated by ${kit.generator || "Ultimate Tokens"}. Apply these tokens; never invent raw values.\n\n` +
      `Systems in this kit: ${[hasColorG && "Color", kit.type && "Typography", kit.geometry && "Geometry"].filter(Boolean).join(" · ") || "—"}.\n\n`;
    if (hasColorG) g +=
      `## Color\n` +
      `- **Surfaces / backgrounds** → the \`*/surface*\` and \`*/background\` roles (resolve per light/dark).\n` +
      `- **Accents** → a palette's prime role (e.g. \`primary/primary\`) and its \`*Dim/Bright/Low/High\` variants.\n` +
      `- **Text on accents** → the \`*/on*\` roles (already contrast-aware).\n` +
      `- **Borders/dividers** → \`*/outline*\`; **overlays** → \`*/scrim*\`.\n` +
      `- Pass a \`scheme\` of \`light\` or \`dark\`; resolve_token gives the exact hex.\n` +
      `- Match an existing colour with \`nearest_token\` before introducing a new one.\n\n`;
    if (kit.type) g +=
      `## Typography\n` +
      `- \`get_type\` / \`brand://type\` → the type scale. Pick a voice by the text's FUNCTION: **Display** (hero), **Heading** (sections), **Lead** (standfirst), **Body** (reading), **Quote** (pull-quotes), **Caption** (figures), **UI** (controls/labels), **Code** (code), **Legal** (fine-print).\n` +
      `- Each voice's step carries \`size\`, \`lineHeight\`, \`letterSpacing\`, \`weight\` — apply them together; don't hand-pick sizes.\n\n`;
    if (kit.geometry) g +=
      `## Geometry\n` +
      `- \`get_geometry\` / \`brand://geometry\` → the size ramp + radius ladder + spacing scale.\n` +
      `- Size a control by its \`height\`; **edge padding = (height − glyph) / 2** (the centering law). Use \`gap\`, \`radius\` (pill = height/2), and \`space\` for layout.\n`;
    return g;
  };

  // ── tools ──
  const TOOLS = [];
  if (hasColor) TOOLS.push(
    { name: "list_palettes", description: "List the brand's palettes with their identity colour.",
      inputSchema: { type: "object", properties: {} },
      run: () => palettes.map((p) => ({ name: p.name, key: p.key, stops: (p.ramp || []).length })) },
    { name: "get_ramp", description: "The full tonal ramp (stops → hex) for one palette.",
      inputSchema: { type: "object", properties: { palette: { type: "string" } }, required: ["palette"] },
      run: (a) => { const p = findPalette(a.palette); return p ? { name: p.name, ramp: p.ramp } : { error: `no palette "${a.palette}"` }; } },
    { name: "resolve_token", description: "Resolve a semantic role to its hex in a scheme. role = \"palette/roleKey\" (or palette + role).",
      inputSchema: { type: "object", properties: { palette: { type: "string" }, role: { type: "string" }, scheme: { type: "string", enum: ["light", "dark"] } } },
      run: (a) => {
        const scheme = a.scheme === "dark" ? "dark" : "light";
        let slug = a.palette, key = a.role;
        if (!a.palette && a.role && a.role.includes("/")) { const [pp, ...rest] = a.role.split("/"); slug = pp; key = rest.join("/"); }
        const r = (roles[slugOf(slug)] || {})[key];
        return r ? { palette: slug, role: key, scheme, hex: r[scheme] ?? r.light } : { error: `no role "${slug}/${key}"` };
      } },
    { name: "get_semantic", description: "All resolved semantic roles for a scheme, as { \"palette/role\": hex }.",
      inputSchema: { type: "object", properties: { scheme: { type: "string", enum: ["light", "dark"] } } },
      run: (a) => semanticFor(a.scheme === "dark" ? "dark" : "light") },
    { name: "nearest_token", description: "The brand token closest to a given hex (so the agent reuses the system instead of inventing a colour).",
      inputSchema: { type: "object", properties: { hex: { type: "string" } }, required: ["hex"] },
      run: (a) => nearestToken(a.hex) || { error: "no palettes" } },
  );
  if (kit.type) TOOLS.push(
    { name: "get_type", description: "The brand's typography scale — treatment, font roles, and the eleven-voice (Display/Heading/Sub-heading/Kicker/Lead/Body/Quote/Caption/UI/Code/Legal) size ramp (size · line-height · letter-spacing · weight).",
      inputSchema: { type: "object", properties: {} }, run: () => kit.type },
  );
  if (kit.geometry) TOOLS.push(
    { name: "get_geometry", description: "The brand's geometry/dimensional scale — the XS–2XL size ramp (height · icon · font · padding · radius), the radius ladder, and the spacing scale.",
      inputSchema: { type: "object", properties: {} }, run: () => kit.geometry },
  );

  // ── resources ──
  const RESOURCES = [
    { uri: "brand://kit", name: "Brand kit (full)", mimeType: "application/json", read: () => JSON.stringify(kit, null, 2) },
  ];
  if (hasColor) RESOURCES.push(
    { uri: "brand://palettes", name: "Palettes + ramps", mimeType: "application/json", read: () => JSON.stringify(palettes, null, 2) },
    { uri: "brand://semantic/light", name: "Semantic roles (light)", mimeType: "application/json", read: () => JSON.stringify(semanticFor("light"), null, 2) },
    { uri: "brand://semantic/dark", name: "Semantic roles (dark)", mimeType: "application/json", read: () => JSON.stringify(semanticFor("dark"), null, 2) },
  );
  if (kit.type) RESOURCES.push({ uri: "brand://type", name: "Typography scale", mimeType: "application/json", read: () => JSON.stringify(kit.type, null, 2) });
  if (kit.geometry) RESOURCES.push({ uri: "brand://geometry", name: "Geometry / dimensional scale", mimeType: "application/json", read: () => JSON.stringify(kit.geometry, null, 2) });
  RESOURCES.push({ uri: "brand://guide", name: "Usage guide", mimeType: "text/markdown", read: usageGuide });

  // ── prompts ──
  const PROMPTS = [
    { name: "apply_brand", description: "Guidelines for applying this brand kit's tokens.",
      get: () => ({ description: "Apply the brand kit", messages: [{ role: "user", content: { type: "text", text: usageGuide() } }] }) },
  ];

  return { kit, palettes, hasColor, TOOLS, RESOURCES, PROMPTS, SERVER, PROTOCOL_VERSION };
}

// handle(msg, surface) → a JSON-RPC 2.0 response OBJECT, or null when nothing should be sent (a notification,
// or a notification-shaped ping). Pure: the caller writes the returned object to its transport.
export function handle(msg, surface) {
  const { TOOLS, RESOURCES, PROMPTS, kit } = surface;
  const m = msg || {};
  const { id, method, params } = m;
  const isRequest = id !== undefined && id !== null;
  const reply = (result) => ({ jsonrpc: "2.0", id, result });
  const fail = (code, message) => ({ jsonrpc: "2.0", id, error: { code, message } });
  const textResult = (obj) => ({ content: [{ type: "text", text: typeof obj === "string" ? obj : JSON.stringify(obj, null, 2) }] });

  switch (method) {
    case "initialize":
      return reply({ protocolVersion: PROTOCOL_VERSION, capabilities: { tools: {}, resources: {}, prompts: {} }, serverInfo: SERVER, instructions: `Brand kit "${kit.name || ""}" from ${kit.generator || "Ultimate Tokens"}. Use resolve_token / get_ramp / nearest_token; read brand://guide first.` });
    case "notifications/initialized": return null; // notification, no reply
    case "ping": return isRequest ? reply({}) : null;
    case "tools/list": return reply({ tools: TOOLS.map(({ name, description, inputSchema }) => ({ name, description, inputSchema })) });
    case "tools/call": {
      const t = TOOLS.find((x) => x.name === (params && params.name));
      if (!t) return fail(-32602, `unknown tool: ${params && params.name}`);
      try { return reply(textResult(t.run((params && params.arguments) || {}))); }
      catch (e) { return reply({ ...textResult(`error: ${e.message}`), isError: true }); }
    }
    case "resources/list": return reply({ resources: RESOURCES.map(({ uri, name, mimeType }) => ({ uri, name, mimeType })) });
    case "resources/read": {
      const r = RESOURCES.find((x) => x.uri === (params && params.uri));
      if (!r) return fail(-32602, `unknown resource: ${params && params.uri}`);
      return reply({ contents: [{ uri: r.uri, mimeType: r.mimeType, text: r.read() }] });
    }
    case "prompts/list": return reply({ prompts: PROMPTS.map(({ name, description }) => ({ name, description })) });
    case "prompts/get": {
      const p = PROMPTS.find((x) => x.name === (params && params.name));
      if (!p) return fail(-32602, `unknown prompt: ${params && params.name}`);
      return reply(p.get((params && params.arguments) || {}));
    }
    default:
      return isRequest ? fail(-32601, `method not found: ${method}`) : null;
  }
}
